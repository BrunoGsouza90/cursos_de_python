var = input('\nDigite algo qualquer: ')
print('O tipo primitivo do que foi digitado é:\033[33m', type(var),'\033[m')
print('É alfanúmerico?\033[31m',var.isalnum(),'\033[m')
print('É alfabético?\033[31m',var.isalpha(),'\033[m')
print('Só tem letras minúsculas?\033[31m',var.islower(),'\033[m')
print('É númerico?\033[31m',var.isnumeric(),'\033[m')
print('Só tem espaços?\033[31m',var.isspace(),'\033[m')
print('Está capitalizada?\033[31m',var.istitle(),'\033[m')
print('Só tem letras maiúsculas?\033[31m',var.isupper(),'\033[m')
